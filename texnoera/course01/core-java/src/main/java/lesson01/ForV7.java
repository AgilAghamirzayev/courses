package lesson01;

public class ForV7 {

  public static void main(String[] args) {

    for (int first = 1; first <= 10; first++) {
      for (int second = 1; second <= 10; second++) {
        System.out.println(first + " x " + second + " = " + second * first);
      }
      System.out.println();
    }

  }

}
